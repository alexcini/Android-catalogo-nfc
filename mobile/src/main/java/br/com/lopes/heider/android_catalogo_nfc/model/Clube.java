package br.com.lopes.heider.android_catalogo_nfc.model;

public class Clube {

    private String nome;
    private String hino;
    private int escudo;
    private int cores;
    private int background;
    private int hinosom;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getHino() {
        return hino;
    }

    public void setHino(String hino) {
        this.hino = hino;
    }

    public int getEscudo() {
        return escudo;
    }

    public void setEscudo(int escudo) {
        this.escudo = escudo;
    }

    public int getCores() {
        return cores;
    }

    public void setCores(int cores) {
        this.cores = cores;
    }

    public int getBackground() {
        return background;
    }

    public void setBackground(int background) {
        this.background = background;
    }

    public int getHinosom() {
        return hinosom;
    }

    public void setHinosom(int hinosom) {
        this.hinosom = hinosom;
    }
}
